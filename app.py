
import streamlit as st
import pandas as pd
import numpy as np
import joblib
from pathlib import Path

# -----------------------------
# Load trained deployment model
# -----------------------------
BASE_DIR = Path(__file__).resolve().parent
MODEL_PATH = BASE_DIR / "models" / "deploy_model.pkl"

model = joblib.load(MODEL_PATH)

# -----------------------------
# Streamlit page setup
# -----------------------------
st.set_page_config(
    page_title="EstateIQ",
    layout="centered"
)

col1, col2 = st.columns([5, 2])

with col1:
    st.title("EstateIQ")

with col2:
    st.caption("📍 Ames, Iowa, USA")
st.write("How much is your house worth?")

# -----------------------------
# Feature order used in training
# -----------------------------
FEATURES = [
    "OverallQual",
    "TotalSF",
    "GarageCars",
    "TotalBathrooms",
    "GrLivArea",
    "HouseAge",
    "Fireplaces"
]

# -----------------------------
# Input form
# -----------------------------
with st.form("prediction_form"):
    overall_qual = st.slider("Overall Quality", 1, 10, 5)
    total_sf = st.number_input(
        "Total Square Footage",
        min_value=300.0,
        max_value=10000.0,
        value=2000.0,
        step=50.0
    )
    garage_cars = st.slider("Garage Capacity", 0, 4, 2)
    total_bathrooms = st.number_input(
        "Total Bathrooms",
        min_value=0.0,
        max_value=10.0,
        value=2.0,
        step=0.5
    )
    gr_liv_area = st.number_input(
        "Above Ground Living Area (sq ft)",
        min_value=300.0,
        max_value=6000.0,
        value=1500.0,
        step=50.0
    )
    house_age = st.number_input(
        "House Age (years)",
        min_value=0,
        max_value=200,
        value=20,
        step=1
    )
    fireplaces = st.slider("Number of Fireplaces", 0, 4, 1)

    submitted = st.form_submit_button("Predict House Price")

with st.sidebar:
    st.header("About")
    st.write(
        """
        This application estimates residential property prices using an
    XGBoost regression model trained on the Ames Housing dataset.
        
        Dataset: Ames Housing
        
        Features Used:
        - Overall Quality
        - Total Square Footage
        - Garage Capacity
        - Total Bathrooms
        - Living Area
        - House Age
        - Fireplaces
        """
    )

st.sidebar.metric(
    "Kaggle RMSLE",
    "0.12595"
)

    
# -----------------------------
# Prediction
# -----------------------------
if submitted:

    input_df = pd.DataFrame([{
        "OverallQual": overall_qual,
        "TotalSF": total_sf,
        "GarageCars": garage_cars,
        "TotalBathrooms": total_bathrooms,
        "GrLivArea": gr_liv_area,
        "HouseAge": house_age,
        "Fireplaces": fireplaces
    }])

    input_df = input_df[FEATURES]

    # Prediction
    predicted_log_price = model.predict(input_df)[0]

    # Convert from log scale
    predicted_price = np.expm1(predicted_log_price)

    # Price range
    lower = predicted_price * 0.90
    upper = predicted_price * 1.10

    st.success(
        f"Estimated Market Value: ${predicted_price:,.0f}"
    )

    st.info(
        f"Likely Price Range: ${lower:,.0f} - ${upper:,.0f}"
    )

    st.caption(
    "Disclaimer: This estimate is generated by a machine learning model trained on historical housing data and is intended for educational and informational purposes only. Actual property values may vary based on market conditions, location-specific factors, and property characteristics not captured by the model."
)